import * as React from "react";
import * as ReactDOM from "react-dom";
import perspective, { Table } from "@finos/perspective";

import "@finos/perspective-viewer";
import "@finos/perspective-viewer-datagrid";
import "@finos/perspective-viewer-d3fc";
import {
  PerspectiveViewerConfig,
  HTMLPerspectiveViewerElement,
} from "@finos/perspective-viewer";

import "./index.css";

const worker = perspective.shared_worker();

const getTable = async (): Promise<Table> => {
  const websocket = perspective.websocket("ws://localhost:8000/websocket");
  const table = await websocket.open_table("data_source_one");
  const view = await table.view();
  return await worker.table(view);
};

const config: PerspectiveViewerConfig = {};

const App = (): React.ReactElement => {
  const viewer = React.useRef<HTMLPerspectiveViewerElement>(null);

  React.useEffect(() => {
    getTable().then((table) => {
      if (viewer.current) {
        viewer.current.load(table);
        viewer.current.restore(config);
      }
    });
  }, []);

  return <perspective-viewer ref={viewer}></perspective-viewer>;
};

window.addEventListener("load", () => {
  ReactDOM.render(<App />, document.getElementById("root"));
});
